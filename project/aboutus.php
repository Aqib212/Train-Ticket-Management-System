	<h2><strong>About National Rail Enquiries</strong></h2>

	<p>We are the definitive source of customer information for all passenger rail services on the National Rail network in England, Wales and Scotland. National Rail Enquiries is part of the Rail Delivery Group (RDG), which provides business services to Train Operating Companies.</p>

	<h5><strong>Introduction</strong></h5>
	
	<p>We handle an average of two and a half million journey planning enquiries every week day through our contact centre, mobile, apps, website and through information services supplied to 3rd parties. We offer journey planning, ticket sales, real time information, fares information and a range of rail-related information to rail customers.</p>
	
	<h5><strong>Listening to our customers</strong></h5>
	
	<p>In addition to the survey comments we also closely monitor feedback (good and bad) received through Facebook, Twitter, our ‘Community’ channel and Customer Relations Department</p>
	
	<p>Changes are continuously being introduced as a result of customer feedback and we’ll soon be letting you know more about these enhancements via our website.</p>
